int	scan_digit(char *buf) {
	int m;
	int i;

	i = 0;
	m = 0;
	while (buf[i] != 0)
	{
		if ((buf[i] >= 48) && (buf[i] <= 57) && (buf[i + 1] >= 48) &&
				(buf[i + 1] <= 57))
			m = buf[i] * 10 + buf[i + 1];
		else if ((buf[i] >= 48) && (buf[i] <= 57))
			m = buf[i];
	}
	return(m);
}
